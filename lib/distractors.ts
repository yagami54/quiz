// تطبيع للمقارنة (تشكيل + همزات + تاء مربوطة + ال التعريف)
function normalize(s: string): string {
  return s
    .replace(/[ً-ٰٟـ]/g, "")
    .replace(/[إأآٱ]/g, "ا")
    .replace(/ى/g, "ي")
    .replace(/ؤ/g, "و")
    .replace(/ئ/g, "ي")
    .replace(/ة/g, "ه")
    .toLowerCase()
    .replace(/[^\p{L}\p{N}]+/gu, " ")
    .trim();
}
const stripAl = (s: string) => s.replace(/^ال/, "");

const INDEX = POOLS.map((p) => ({
  ...p,
  set: new Set(p.detect.flatMap((x) => [normalize(x), stripAl(normalize(x))])),
}));

function inSet(set: Set<string>, opt: string): boolean {
  const n = normalize(opt);
  return set.has(n) || set.has(stripAl(n));
}

/** تضارب: الخيار المرشّح يساوي/يحتوي/محتوى في أحد الخيارات الأصلية (يمنع تكرار الجواب بصيغة أخرى) */
function conflicts(cand: string, options: string[]): boolean {
  const c = stripAl(normalize(cand));
  if (c.length < 2) return true;
  for (const o of options) {
    const n = stripAl(normalize(o));
    if (!n) continue;
    if (n === c) return true;
    if (n.length >= 3 && c.includes(n)) return true;
    if (c.length >= 3 && n.includes(c)) return true;
  }
  return false;
}

// أسئلة لا يجوز توسيعها (اختيار "أيّ من هذه ينتمي للفئة" أو خيارات شمولية)
const SKIP_Q = /(أي|اي|أيّ|أيٌّ)\s*(من|مما)\s*(هذه|هؤلاء|التالي|يلي)/;
const SKIP_OPT = /(كل ما سبق|كلها|كلاهما|جميع ما|كل السور)/;

/**
 * يعيد حتى `count` خيارًا خاطئًا من نفس نوع جواب السؤال (مع مراعاة سياقه)،
 * أو [] إن لم يتأكّد من النوع — الأفضل البقاء على 4 من اختلاق خيارات غريبة.
 */
export function pickTextDistractors(q: Question, count: number): string[] {
  if (SKIP_Q.test(q.text)) return [];
  if (q.options.some((o) => SKIP_OPT.test(o))) return [];

  // المجمع الأنسب: أكثر تطابقًا، وعند التعادل الأصغر (الأكثر تخصّصًا)
  let best: (typeof INDEX)[number] | null = null;
  let bestHits = 0;
  for (const entry of INDEX) {
    let hits = 0;
    for (const o of q.options) if (inSet(entry.set, o)) hits++;
    if (hits > bestHits || (hits === bestHits && hits >= 3 && best && entry.detect.length < best.detect.length)) {
      bestHits = hits;
      best = entry;
    }
  }
  if (!best || bestHits < 3) return [];

  // قائمة الإضافة: refine حسب نص السؤال ← add ← detect
  let addList = best.add ?? best.detect;
  if (best.refine) {
    for (const r of best.refine) {
      if (r.test.test(q.text)) {
        addList = r.add;
        break;
      }
    }
  }

  const candidates = addList.filter((c) => !conflicts(c, q.options));
  for (let i = candidates.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [candidates[i], candidates[j]] = [candidates[j], candidates[i]];
  }
  return candidates.slice(0, count);
}
